<script type="text/javascript">
    $(function() {
        let jForm = $('#contact-form'),
            rcForm = recharge.validations.addValidations(jForm),
            submitButton = $('#submit-button');

        submitButton.click(function(e) {
        	e.preventDefault();
            if (rcForm.validate()) {
            	console.info('validation success');
            	rc_submitForm();
            } else {
                console.info('validation failed');
            }
        });

        $('.recharge-withplus-button').click(function(e) {
            e.preventDefault();

            if ($(window).width() < 767) {
                let index = window.location.href.indexOf('#');
                if (index === -1) {
                    window.location.href += '#recharge-withplus-card';
                } else {
                    window.location.href = window.location.href.substr(0, index) + '#recharge-withplus-card'; 
                }
            } else {
                window.location = $(this).attr('href');
            }
        });

        function rc_submitForm(){
        	var request,
		    	$form = $('#contact-form');
		    	serializedData = $form.serialize();
		    	$inputs = $form.find('input, select, button, textarea');

		    $inputs.prop("disabled", true);

		    $.ajax({
		        url: '/thank-you-template.php',
		        type: 'post',
		        data: serializedData
		    }).done(function (response, textStatus, jqXHR){
		    	$('.form-page-container').hide();
		    	$('.thank-you-container').show();
		    }).fail(function (jqXHR, textStatus, errorThrown){
		        console.error('The following error occurred: '+textStatus, errorThrown);
		    }).always(function () {
		        $inputs.prop('disabled', false);
		    });
		}
    });
</script>

