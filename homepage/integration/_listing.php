<script>
	function setMinHeight() {
		var integrationCardHeight = -1;
		$('.integration__item').each(function() {
			if (integrationCardHeight > $(this).height()) {
				integrationCardHeight = integrationCardHeight;
			} else {
				integrationCardHeight = $(this).height();
			}
		});
		$('.integration__item').animate({'height': integrationCardHeight + 20}, 200);
	}
	$(function() {
		setMinHeight()
		$(window).resize(function() {
			setMinHeight();
		});
	});
</script>